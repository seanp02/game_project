def user_attack(usrname, usratk, mobname, mobhp):
    print(f"{usrname}의 공격!")
    i = random.randint(1, 10)
    if i <= 3:
        damage = usratk
        print("일반 공격 성공!")
    elif 3 < i < 8:
        damage = int(usratk * 1.5)
        print("크리티컬!")
    else:
        damage = usratk * 2
        print("크리티컬!!!!")
    mobhp -= damage
    if mobhp <= 0:
        print(f"{mobname}에게 {damage}의 데미지를 입혔습니다!\n")
        return False
    else:
        print(f"{mobname}에게 {damage}의 데미지를 입혔습니다!\n")
    return mobhp

