import random

class Player:
    name = None
    base_hp = None
    hp = None
    mp = None
    power = None
    weapon = None
    armor = None
    item = [None]

    # 상태 출력
    def status(self):
        print("\n상태창")
        print("===================")
        print('= name:',self.name)
        print('= hp:',self.hp)
        print('= mp:',self.mp)
        print('= power:',self.power)
        print('= item:',self.item)
        print("===================")
        
    def equip_weapon(self, weapon):
        if self.weapon is not None:
            print(f"현재 장착중인 무기: {self.weapon.name} (공격력: {self.weapon.min_attack} ~ {self.weapon.max_attack})")
            print(f"새로 장착하려는 무기: {weapon.name} (공격력: {weapon.min_attack} ~ {weapon.max_attack})")
            choice = input("새 무기를 장착하겠습니까? (y/n): ")
            if choice.lower() != "y":
                print("무기 변경을 취소하셨습니다.")
                return
        self.weapon = weapon
        self.power = random.randint(weapon.min_attack,weapon.max_attack)
        print(f"새로운 무기 '{weapon.name}'를 장착하였습니다. 공격력이 {weapon.min_attack} ~ {weapon.max_attack}으로 변경되었습니다.")

    def equip_armor(self, armor):
            if self.armor is not None:
                print(f"현재 장착중인 방어구: {self.armor.name} (체력 증가량: {self.armor.health_increase})")
                print(f"새로 장착하려는 방어구: {armor.name} (체력 증가량: {armor.health_increase})")
                  
                choice = input("새 방어구를 장착하겠습니까?  (y/n): ")
                if choice.lower() != "y":
                    print("방어구 변경을 취소하셨습니다.")
                    return
            self.armor = armor
            self.hp = self.base_hp + armor.health_increase
            print(f"새로운 방어구 '{armor.name}'를 장착하였습니다. 모두 회복하여 체력이 {self.hp}로 변경되었습니다.")

    def attack(self):
        if self.weapon:
            return random.randint(self.weapon.min_attack, self.weapon.max_attack)
        else:
            return self.power

